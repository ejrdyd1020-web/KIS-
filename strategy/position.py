def is_buyable_time() -> bool:
    """
    15:20 이후에는 신규 매수 차단
    매수 진입 전 이 함수를 먼저 호출해서 확인

    사용 예시 (매수 로직에서):
        if not is_buyable_time():
            return  # 매수 스킵

    Returns:
        True  → 매수 가능 시간 (15:20 이전)
        False → 매수 불가 (15:20 이후)
    """
    now = datetime.now().strftime("%H:%M")
    if now >= NO_BUY_AFTER:
        logger.debug(f"[매수 차단] {now} → 15:20 이후 신규 매수 금지")
        return False
    return True


